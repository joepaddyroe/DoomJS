/**
 * Draw indexed-color text onto the software framebuffer.
 */
export class TextRenderer {
  /**
   * @param {Uint8Array} pixels
   * @param {number} x
   * @param {number} y
   * @param {string} text
   * @param {number} color
   * @param {number} [scale=1]
   */
  static drawString(pixels, x, y, text, color, scale = 1) {
    let cursor = x;
    const upper = text.toUpperCase();
    for (let i = 0; i < upper.length; i++) {
      const ch = upper[i];
      if (ch === '\n') {
        y += 8 * scale;
        cursor = x;
        continue;
      }
      TextRenderer.drawChar(pixels, cursor, y, ch, color, scale);
      cursor += 6 * scale;
    }
  }

  /**
   * @param {Uint8Array} pixels
   * @param {number} x
   * @param {number} y
   * @param {string} ch
   * @param {number} color
   * @param {number} scale
   */
  static drawChar(pixels, x, y, ch, color, scale) {
    const glyph = GLYPHS[ch] ?? GLYPHS[' '];
    for (let row = 0; row < 7; row++) {
      const bits = glyph[row];
      for (let col = 0; col < 5; col++) {
        if ((bits >> (4 - col)) & 1) {
          TextRenderer.fillBlock(pixels, x + col * scale, y + row * scale, scale, scale, color);
        }
      }
    }
  }

  /**
   * @param {Uint8Array} pixels
   * @param {string} text
   * @param {number} y
   * @param {number} color
   * @param {number} [scale=1]
   */
  static drawCentered(pixels, text, y, color, scale = 1) {
    const width = text.length * 6 * scale - scale;
    const x = ((SCREENWIDTH - width) / 2) | 0;
    TextRenderer.drawString(pixels, x, y, text, color, scale);
  }

  /**
   * @param {Uint8Array} pixels
   * @param {number} x
   * @param {number} y
   * @param {number} w
   * @param {number} h
   * @param {number} color
   */
  static fillBlock(pixels, x, y, w, h, color) {
    for (let row = y; row < y + h; row++) {
      if (row < 0 || row >= 200) {
        continue;
      }
      for (let col = x; col < x + w; col++) {
        if (col >= 0 && col < SCREENWIDTH) {
          pixels[row * SCREENWIDTH + col] = color;
        }
      }
    }
  }
}
